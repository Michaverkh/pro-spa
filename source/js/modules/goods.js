const goodListContainer  = document.querySelector('.goods__list');
const goodTemplate = document.querySelector('#goods-template')
  .content
  .querySelector('.good-item');

const createGood = (good) => {
  const goodElement = goodTemplate.cloneNode(true);

  goodElement.querySelector('.good-item__type').textContent = good.type;
  goodElement.querySelector('.good-item__image').src = good.img;
  goodElement.querySelector('.good-item__image--webp').srcset = good.imgWebp;
  goodElement.querySelector('.good-item__name').textContent = good.name;
  goodElement.querySelector('.good-item__area').textContent = good.area;
  goodElement.querySelector('.good-item__location').textContent = good.location;
  goodElement.querySelector('.good-item__company').textContent = good.company;

  if(good.isNew === true) {
    goodElement.classList.add('good-item--new')
  };

  const commentFeature = goodElement.querySelector('.good-item__feature--comment');
  if(good.commentsCount > 0) {
    commentFeature.classList.add('feature--visible');
    goodElement.querySelector('.feature__counter--comment').textContent = good.commentsCount;
  }

  const likeFeature = goodElement.querySelector('.good-item__feature--like');
  if(good.likesCount > 0) {
    likeFeature.classList.add('feature--visible');
    goodElement.querySelector('.feature__counter--like').textContent = good.likesCount;
  }

  return goodElement;
};

export const renderGoods = (data) => {
  data.forEach(good => {
    const currentItem = createGood(good);
    goodListContainer.appendChild(currentItem);
  });
}

export const removeGoods = () => {
  while (goodListContainer.firstChild) {
    goodListContainer.removeChild(goodListContainer.lastChild);
  }
}

